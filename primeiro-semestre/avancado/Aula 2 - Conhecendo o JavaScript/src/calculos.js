// Soma - utiliza o sinal de mais para somar
var soma = 10 + 150;

// Divisão - divide utilizando o sinal de barra (comum)
var divisao = 40 / 30;

// Subtração - subtrai utilizando o sinal de menos ou traço
var subtracao = 50 - 5;

// Multiplica utilizando o sinal asterisco
var multiplicacao = 100 * 33;

// Exponenciacao - eleva o numero da esquerda ao da direita
var exponenciacao = 100 ** 3;

var cem = 100;

var expoMath = Math.pow(cem, 3);

// Modulus - também conhecido como Resto de Divisão
var restoDivisao = 200 % 2;

// Incremento - soma mais 1 a cada iteração
var incremento = soma++;

incremento += soma;

// Decremento - subtrai mais 1 a cada iteração
var decremento = subtracao--;

decremento -= subtracao;

